class Node:
    def __init__(self, value):
        self.value = value
        self.next = None
        

class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node

        
    def append(self, value):
        new_node = Node(value)
        if self.head == None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        return True
        

    def find_middle_node(self):
        new_node1 = self.head
        new_node2 = self.head
        while(new_node2.next is not None and new_node2 is not None):
            new_node1 = new_node1.next
            new_node2 = new_node2.next.next
        return new_node1




my_linked_list = LinkedList(1)
my_linked_list.append(2)
